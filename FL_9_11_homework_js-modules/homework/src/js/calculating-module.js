export const calcBasicMathematicalOperations = {
  'addition': function(first, second) {
    return first + second;
  },

  'subtraction': function(first, second) {
    return first - second;
  },

  'multiplication': function(first, second) {
    return first * second;
  },

  'division': function(first, second) {
    return first / second;
  }
}
